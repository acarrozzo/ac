<h3>Card details</h3>
<img class="right-img" src="img/tactics-card-anatomy-.jpg" width="750" height="750" alt="LG TACTICS - strategy battle card game"/>
<img class="right-img" src="img/tactics-card-anatomy-2.jpg" width="750" height="750" alt="LG TACTICS - strategy battle card game"/>

<p>There are only 4 points of interest on each card.</p>
<ul>
<li><p><strong>Power</strong> - The big number is the power level of the card. The higher the number, the stronger the card.</p></li>
<li><p><strong>Name</strong> - The name is at the top of the card.</p></li>
<li><p><strong>Color</strong> - At the bottom of the card is the color with identifying icon.</p></li>
<li><p><strong>Deploy Requirement</strong> - Some cards, in order to be deployed, require other card(s) to be on the battlefield. For example, in order to deploy a card that requires another blue card, YOU MUST HAVE A BLUE CARD on the battlefield when you deploy.</p></li>
</ul>


