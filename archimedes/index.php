    <strong>LG TACTICS</strong>
<h2>Full Game Rules</h2>
<p>Basic gameplay involves picking up 3 cards, deploying 3 cards to the table, and then attacking with any 3 cards that have been on the table since last turn.</p>

<br/><hr>
<h3>Objective</h3>
<img class="right-img" src="img/tactics-psd-table5.jpg" width="960" height="540" alt="LG TACTICS - strategy battle card game"/>

<!--
<img class="right-img" src="img/tactics-psd-sq-table1.jpg" width="600" height="600" alt="LG TACTICS - strategy battle card game"/>

<img class="right-img" src="img/tactics-psd-table-deck_1.jpg" width="600" height="600" alt="LG TACTICS - strategy battle card game"/>

<img class="right-img" src="img/tactics-psd-2-final-poster.jpg" width="960" height="540" alt="LG TACTICS - strategy battle card game"/>
-->


<ul>
  <li><p>Overwhelm your enemies.</p></li>
  <li><p><strong>To win the round, deploy cards onto the battlefield that add up to 11 or more AND are all the SAME COLOR</strong>.</p></li>
  <li><p>The moment you have cards on the table that are the same color and add up to 11 or more you win the round.</p></li>
  <li><p> First player to win 3 rounds, wins the game.</p></li>
</ul>

<br/><hr>
<h3>Setup</h3>
<p>Shuffle the deck and deal 3 cards to each player. Place remaining deck in center and take turns around the table. Discard cards face up in a pile next to the deck.</p>

<br/><hr>
<h3>Gameplay</h3>
<img class="right-img" src="img/tactics-psd-table3.jpg" width="960" height="540" alt="LG TACTICS - strategy battle card game"/>

<h3>ON YOUR TURN do these 3 steps:</h3>
<p><strong>1. RECRUIT - Pick up 3 cards</strong></p>
<ul>
  <li><p>Underdog : Pick up another card if you have the least number of cards on the battlefield. Ties do not count.</p></li>
</ul>
<p><strong>2. DEPLOY - Play up to 3 cards onto the battlefield.</strong></p>
<ul>
  <li><p>On your turn you can play up to 3 cards onto the battlefield. You can play almost any card in your hand, but certain cards have deploy requirements.</p></li>
  <li><p>If a card has a deploy requirement, you must have the required card(s) on the battlefield to deploy.</p>
</li>
</ul>
<p><strong>3. ATTACK - Attack with up to 3 cards.</strong></p>
<ul>
<li><p>Cards cannot attack during the turn they are deployed.</p></li>
<li><p>An individual card can only attack once per turn.</p></li>
<li><p>When two cards battle during an attack, the lowest valued card is discarded and the largest valued card remains.</p></li>
<li><p>If the cards have the same value, both are discarded.</p></li>
<li><p>Multiple cards can attack a single card to defeat it. For example, a 2 and a 2 can attack a 3 and all cards will be discarded.</p></li>
<li><p>You can attack multiple players and multiple cards during the same turn.</p></li>
<li><p>If the deck runs out of cards, shuffle the discard pile and use it as the new deck.</p></li>
</ul>
<br/><hr>
<h3>Table setup</h3>
<img class="right-img" src="img/tactics-how-to-play2.jpg" width="449" height="593" alt="LG TACTICS - strategy battle card game"/>
<p>Cards cannot attack the turn they are deployed. You can use this double row method to help remember which cards can and cannot attack.</p>
<br/><hr>
<h3>Alternative gameplay (Fast)</h3>
<p>For a faster and easier game, Recruit, Deploy and Attack with 2 cards instead of 3. To win, you need cards on the battlefield of matching color that add up to 7 instead of 11.</p>
<br/><hr>
<h3>Solo Gameplay</h3>
<p>For Solo Gameplay you will be trying to create as many same colored stacks of 11 as you can before the deck runs out.</p>
<p>Play the game as you normally would but do this during your turn:</p>
<ul>
  <li><p>To start your turn pick up 3 cards like you normally would. Take note of the color and number of the 3rd card you pick up. This card represents an attack against you.</p></li>
<li><p>You must discard a card on the battlefield with the same number and color as that card.</p></li>
<li><p>For example: If the card is a Red 3, you must discard a Red 3 that you have on the battlefield.</p></li>
<li><p>If you don't have a Red 3 on the battlefield, then you look for a Red 2 to discard.</p></li>
<li><p>If you don't have a Red 2 on the battlefield, then you look for a Red 1 to discard.</p></li>
<li><p>If you don't have any Reds at all, then you don't have to discard anything (phew).</p></li>
<li><p>And if you only have a Red 4 on the battlefield, then you don't have to discard anything as well.</p></li>
</ul>
<p>This essentially acts as an attack on your cards, slowly chipping away at your advances.</p>
<p>When the deck runs out, you deploy all the possible cards in your hand. After you have deployed all possible cards, count the number of 11-stacks you have created. Check the scores below to see how well you did.</p>
<ul>
  <li><p>6 or less - Weak sauce</p></li>
<li><p>7 - Could do better</p></li>
<li><p>8 - Almost there</p></li>
<li><p>9 stacks (max) - LG TACTICS SOLO GENIUS. nice work.</p></li>
</ul>
<p>And that's a variation on gameplay that lets you play solo. Also a nice way to organize your cards by color. </p>
